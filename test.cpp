#include <iostream>
using namespace std;

int main() {
    int a, b, sum;

    // Nhập hai số
    cout << "Nhập số thứ nhất: ";
    cin >> a;
    cout << "Nhập số thứ hai: ";
    cin >> b;

    // Cộng hai số
    sum = a + b;

    // Hiển thị kết quả
    cout << "Tổng của " << a << " và " << b << " là: " << sum << endl;

    return 0;
}

